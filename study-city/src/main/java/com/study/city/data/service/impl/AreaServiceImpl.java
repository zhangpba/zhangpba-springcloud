    /**
     * 批量插入
     */
    @Override
    public void batchAddArea(List<Area> areas) {
        if (areas != null && areas.size() > 0) {
            logger.info("一共{}条数据需要插入表中", areas.size());
            areas.forEach(city -> {
                Area area = areaMapper.getAreaByCode(city.getCode());
                if (area == null) {
                    areaMapper.addArea(city);
                }
            });
        }
    }

